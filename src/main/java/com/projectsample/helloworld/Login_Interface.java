    private void checkPasswordPeekLabel() {
        if (new String(passwordField2.getPassword()).isEmpty())
            Card.changeCard(passwordPeekBGPanel2, thirdBGPanel2);
        if (new String(confirmPasswordField.getPassword()).isEmpty())
            Card.changeCard(passwordPeekBGPanel3, thirdBGPanel3);
        if (!(new String(passwordField2.getPassword()).isEmpty()))
            Password.hidePassword(passwordPeekBGPanel2, firstBGPanel2, passwordField2);
        if (!(new String(confirmPasswordField.getPassword()).isEmpty()))
            Password.hidePassword(passwordPeekBGPanel3, firstBGPanel3, confirmPasswordField);
        for (int i = 0; i < 3; ++i)
            Validate.setBooleanPasswordPeek(false, i);
    }
    
