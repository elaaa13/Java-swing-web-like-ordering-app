    private void continueLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_continueLabelMouseClicked
        // TODO add your handling code here:
        if ((Validate.validateName(firstNameField.getText())) &&
            (Validate.validateName(lastNameField.getText())) &&
            (Validate.validateEmail(connection, emailAddressField2)) &&
            (Validate.validateContactNumber(contactNumberField.getText())) &&
            (Validate.validatePassword(passwordField2, confirmPasswordField))) {
            Card.changeCard(rightPanel, thirdCard);
            contactNumberField.setText(contactNumberField.getText().replaceAll("\\s", ""));
        }
            
        else if ((Validate.validateName(firstNameField.getText()) == false) ||
            (Validate.validateName(lastNameField.getText()) == false) ||
            (Validate.validateEmail(connection, emailAddressField2) == false) ||
            (Validate.validateContactNumber(contactNumberField.getText()) == false) ||
            (Validate.validatePassword(passwordField2, confirmPasswordField) == false)) {
            if (Validate.validatePassword(passwordField2, confirmPasswordField) == false)
                registerErrorLabel.setText("Passwords do not match.");
            else if (Validate.getEmailBoolean() == true)
                registerErrorLabel.setText("       " + "Email already in use.");
            else
                registerErrorLabel.setText("          " + "Invalid credentials.");
        }
    }//GEN-LAST:event_continueLabelMouseClicked

